{
    cout << "Temperature in Celsius: " << tc << endl; // виводить обчислену температуру в Цельсіях на консоль, використовуючи змінну tc, яка містить результат перетворення температури з Фаренгейта в Цельсій
}

double FahrenheitToCelsius(double tf)
{
    return (tf - 32) * 5.0 / 9.0; // перетворює температуру з Фаренгейта в Цельсій, використовуючи формулу перетворення: C = (F - 32) * 5 / 9, де tf - це температура в Фаренгейтах, яка передається як аргумент, і повертає результат у градусах Цельсія
}

