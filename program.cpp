int DaysInMonth(TDate D)
{
    int daysInMonth[] = { 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31 }; // масив, який містить кількість днів у кожному місяці для звичайного року
    
    if (D.Month == 2 && isLeapYear(D.Year)) // перевірка, чи є місяць лютий і чи є рік високосним, щоб визначити, чи потрібно враховувати 29 днів у лютому
        return 29;
 
    return daysInMonth[D.Month - 1]; // повертаємо кількість днів у місяці, використовуючи індекс місяця (від 1 до 12) для доступу до відповідного елемента масиву daysInMonth, який містить кількість днів у кожному місяці для звичайного року 
}

bool isLeapYear(int year)
{
    return (year % 4 == 0 && year % 100 != 0) || (year % 400 == 0); // перевірка, чи є рік високосним, використовуючи правила високосного року: рік є високосним, якщо він кратний 4, але не кратний 100, або якщо він кратный 400
}

