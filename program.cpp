void Processing2(TTemperature temp, double tf, double &tc)
{
    tc = FahrenheitToCelsius(tf); // викликає метод FahrenheitToCelsius, який перетворює температуру з Фаренгейта в Цельсій, передаючи введену температуру в Фаренгейтах (tf) як аргумент, і зберігає результат у змінній tc, яка передається за посиланням, щоб зміни, внесені в цю змінну всередині методу, були видимі поза межами цього методу

    temp.tf = tf; // зберігає введену температуру в Фаренгейтах у полі tf структури temp
    temp.tc = tc; // зберігає обчислену температуру в Цельсіях у полі tc структури temp
}

